int	check_opposite(char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 0;
	b = 0;
	while (i < 7)
	{
		a = str[2 * i] - '0';
		b = str[2 * i + 8] - '0';
		if ((a + b) < 3 || (a + b) > 5)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
